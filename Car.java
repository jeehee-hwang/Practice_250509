package org.study_250509;

public class Car {

    //Sonata,Bus 부모 클래스
    
    public void start() {
        System.out.println("차에 시동을 걸고 있습니다.");
    }
    public void drive() {
        System.out.println("차가 도로를 달리고 있습니다.");
    }

    public void stop() {
        System.out.println("차가 정지했습니다.");
    }
    public void turnoff() {
        System.out.println("차의 시동을 껐습니다.");
    }

}
